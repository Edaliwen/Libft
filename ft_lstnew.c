#include "libft.h"

t_list	*ft_lstnew(void *content)
{
	t_list *new;

	if (!(new = malloc(sizeof(t_list))))
		return (NULL);
	else
	{
		new->content = content;
		new->next = NULL;
	}
	return (new);
}
